@TeleOp
public class MainBlue extends LinearOpMode {
    @Override
    public void runOpMode() throws InterruptedException {
        MainRobot robot = new MainRobot(20, hardwareMap, gamepad2, telemetry);
        waitForStart();

        try {
            robot.startThreads();
            while (opModeIsActive()){
                robot.opMode(telemetry);
            }
        } finally {
            robot.stopThreads();
        }
    }
}
